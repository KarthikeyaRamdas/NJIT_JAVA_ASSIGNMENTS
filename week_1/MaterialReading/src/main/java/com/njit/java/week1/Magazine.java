package com.njit.java.week1;

public class Magazine {

	public Magazine(String title, String publisher, String editor) {
		System.out.println("Title: " + title);
		System.out.println("Publisher: " + publisher);
		System.out.println("Editor: " + editor + "\n");
	}

	public void magazinesDisplay(String title, String publisher, String editor) {

		System.out.println("Title: " + title);
		System.out.println("Publisher: " + publisher);
		System.out.println("Editor: " + editor + "\n");
	}

}
